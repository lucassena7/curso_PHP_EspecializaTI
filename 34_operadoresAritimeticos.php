<?php

//Operador de atribuição '='
$valor = 12;

echo "$valor <br><hr>";


//Operador aritimético '+='
$valor += 14; //É a mesma coisa que fazer $valor = $valor + 14, ou seja, ele pega o valor atual e soma com o valor.

echo "$valor <br><hr>";


//Operador aritimético '-='
$valor -= 14; //É a mesma coisa que fazer $valor = $valor - 14, ou seja, ele pega o valor atual e diminui do valor.

echo "$valor <br><hr>";


//Operador aritimético '*='
$valor *= 2; //É a mesma coisa que fazer $valor = $valor * 2, ou seja, ele pega o valor atual e multiplica pelo valor.

echo "$valor <br><hr>";


//Operador aritimético '/='
$valor /= 2; //É a mesma coisa que fazer $valor = $valor / 2, ou seja, ele pega o valor atual e divide pelo valor.
echo "$valor <br><hr>";